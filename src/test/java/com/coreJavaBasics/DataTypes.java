package com.coreJavaBasics;

public class DataTypes {
	
//	package, public, static, class are all keywords. keywords starts with small case
	
//	public private keywords are access modifiers

	public static void main(String[] args) {
		
		// Integer - Data Types (Accepts only numeric)
		
		int i = 10;
		int j = 20;
		int k = 30;
		
		System.out.println(i);

		
		// Double - Data Types (Accepts only numeric with and without decimal values)
		
		double d = 10.00;
		double d2 = 20.00;
		double d3 = 30;
		
		System.out.println(d2);
		
		// Char = Data Types (Accepts only one character mentioned with in single quotes. Character can be numbers, alphabets, special characters)
		
		char c = 'a';
		char c2 = 'b';
		char c3 = '1';
		char c4 = '&';
		
		System.out.println(c2);
		
		// Boolean - Data Types (Accepts only true or false)
		
		boolean b = true;
		boolean b2 = false;
		
		System.out.println(b2);
		
		// String - string is a class not a data type (Accepts any character with in double quotes. Character can be numbers, alphabets, special characters)
		
		String s = "Hello World";
		String s2 = "Training";
		String s3 = "Welcome to the future";
		String s4 = "1000.00";
		
		System.out.println(s3);
	}

}
